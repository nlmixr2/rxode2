  double  __DDtStateVar_3__;
  double C2 = NA_REAL;
  double centr = NA_REAL;
  double V2 = NA_REAL;
  double C3 = NA_REAL;
  double peri = NA_REAL;
  double V3 = NA_REAL;
  double depot = NA_REAL;
  double KA = NA_REAL;
  double CL = NA_REAL;
  double Q = NA_REAL;
  double eff = NA_REAL;
